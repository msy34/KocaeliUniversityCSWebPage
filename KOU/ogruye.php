		$.ajax({
			url: 'getuser.php',
			type: 'POST',
			 data: { id: uid, tur: tip},
			 
			dataType: 'html'
		})
		.done(function(data){
			console.log(data);	
			$('#dynamic-content').html('');    
			$('#dynamic-content').html(data); // load response 
			$('#modal-loader').hide();		  // hide ajax loader	
		})
		.fail(function(){
			$('#dynamic-content').html('Bir aksilik var...');
			$('#modal-loader').hide();
		});
		
	});
	
});

