	$(document).on('click', '#getUser', function(e){
		
		e.preventDefault();
		
		var uid = $(this).data('id');   // it will get id of clicked row
		var tip=$(this).data('tip');
		
		$('#dynamic-content').html(''); // leave it blank before ajax call
